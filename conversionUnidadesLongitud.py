# Escriba un programa que convierta de centímetros a pulgadas. Una pulgada es igual a 2.54 centímetros.

longitud = float(input("Ingrese el valor de la longitud en centímetros:  \n"))

proceso = (longitud * 1)/2.54

print(f"La longitud {longitud} en pulgadas es:  {proceso}")


