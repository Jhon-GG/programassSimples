# Escriba un programa que calcule el promedio de 4 notas ingresadas por el usuario:

var1 = float(input("Ingrese la primer nota: \n"))
var2 = float(input("Ingrese la segunda nota: \n"))
var3 = float(input("Ingrese la tercer nota: \n"))
var4 = float(input("Ingrese la cuarta nota: \n"))

promedio = (var1 + var2 + var3 + var4) / 4

print(f"El promedio de sus notas es: {promedio}")